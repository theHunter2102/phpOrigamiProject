                <img src="https://origami.me/wp-content/uploads/2015/10/open-sink-06-min.jpg" class="mb-4 img-fluid"
                    alt="guide" width="400" height="400" loading="lazy">
                <p><strong>Step 6)</strong> The easiest way to make the Open Sink in this example is to open the paper

<section id="history" class="text-center py-4">
    <article class="container-custom my-4">
        <div class="row">
            <h2 class="h2 fw-bold mb-4">History of origami</h2>
            <p class="mb-4">
                Writing a comprehensive history of paper folding is almost impossible, since information about the
                art
                form prior to the 15th century is virtually nonexistent. There are many plausible assertions about
                its
                origins and early history, but most of those are based on little firm documentation. Many studies
                assert
                that origami was invented by the Japanese about a thousand years ago, but its roots may well be in
                China. It is also highly probable that the process of folding was applied to other materials before
                paper was invented, so the origins of recreational folding may lie with cloth or leather. Certainly,
                within Europe, the practise of napkin folding and cloth pleating were held in high esteem. However,
                paper has proved to be the ideal material to fold, and so it is logical to assume that paper folding
                followed the discovery of the papermaking process.
            </p>
        </div>


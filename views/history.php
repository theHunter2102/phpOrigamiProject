            <div class="col-12 col-sm-6 align-self-center justify-content-md-center">
                <div class="steps__content-width">
                    <h3 class="fw-bold mb-4">Akira Yoshizawa (1911-2005)</h3>
                    <p class="mb-4">Akira Yoshizawa was the father of modern origami. He inspired a national and
                        international interest in the art of origami. Yoshizawa developed the current standard for
                        diagraming origami instructions and designed countless patterns. He avoided the accepted cutting
                        techniques used in traditional origami and developed a wet folding technique which rendered
                        softer more naturalistic lines. Yoshizawa published ”Origami Tokuhon” in 1957 which brought him
                        great renown. He published many more books thereafter and later served as a cultural ambassador
                        for Japan. In 1983 he received the honor of Japan’s Order of the Rising Sun. </p>
                </div>
            </div>
        </div>

        <div class="row">
            <div class="col-12 col-sm-6 d-md-flex justify-content-md-center">
                <img src="../public/images/invention2.jpg" alt="invention2"
                    class="img-fluid pb-4 steps__section-thumbnail" width="553" height="746" loading="lazy">
            </div>
            <div class="col-12 col-sm-6 align-self-center justify-content-md-center">
                <div class="steps__content-width">
                    <h3 class="fw-bold mb-4">Kosho Uchiyama (1912-1998)</h3>
                    <p class="mb-4">Kosho Uchiyama was a Buddhist priest and origami master from Japan. He published
                        ”Origami Zukan” a children’s instructional book for origami in 1958. Unlike the standard today,
                        Uchiyama’s book allowed for some cutting. He went on to publish several more books of origami,
                        following in the footsteps of his father Michio Uchiyama who also wrote several origami books.
                        Though Kosho Uchiyama’s folds were not as elegant as Akira Yoshizawa, he was considered
                        contemporary rival of Yoshizawa. Like Uchiyama, his books were translated and shared with
                        international audiences.</p>
                </div>
            </div>
        </div>

<!-- Styles Section Start -->
<section id="style" class="text-center py-4">
    <article class="container-custom my-4">
        <div class="row">
            <h2 class="h2 fw-bold mb-4">Styles</h2>
            <img src="" alt="">
            <p class="mb-4">
                Origami, like other art forms, has many styles. The more common ones include:
            </p>
        </div>
        <div class="row text-start px-2">
            <ol>
                <li>
                    <p>Realistic: Creations that exhibit the main features of the subject, often resulting in
                        complex
                        designs
                        with many steps.</p>
                </li>
                <li>
                    <p>Minimal: Creations that capture the essence of the subject with minimal folds and with an
                        emphasis on
                        simplicity.</p>
                </li>
                <li>
                    <p>Modular: Multiple geometric "units" made from multiple sheets of paper whose flaps and
                        pockets
                        tuck
                        into each other to form polygons or polyhedra. Typically, all sheets are folded in the same
                        way
                        or in a
                        small number of ways.</p>
                </li>
                <li>
                    <p>Composite: As with modular origami, multiple sheets of paper are used, but in this style each
                        sheet is
                        folded differently to realize a different part of the subject. Composite origami was one of
                        the
                        most
                        common styles in the 1950s and ’60s but is relatively uncommon today.</p>
                </li>
                <li>
                    <p>Practical: Models that have a real-life application, such as for use as envelopes, boxes,
                        cups,
                        dishes, etc.</p>
                </li>
                <li>
                    <p>Pureland: A concept suggested by John Smith of England, who proposed a <a target="_blank"
                            href="https://www.merriam-webster.com/dictionary/composition">composition</a> system
                        using
                        only
                        square paper and “mountain” and “valley” folds, resulting in models that are easy to
                        duplicate.
                    </p>
                </li>
                <li>
                    <p>Tessellations: A geometric folding technique in which the image is created by the pattern of
                        folded
                        edges across the paper. Tessellations are often periodic (repeating) and may be flat or
                        three-dimensional, and many of them exhibit further structure when held up to the light. Not
                        surprisingly, many of the leading practitioners of this technique have been mathematicians.
                    </p>
                </li>
                <li>
                    <p>Wet folding: A technique invented by Akira Yoshizawa in which the paper contains a
                        water-soluble
                        glue
                        (known as sizing) and is dampened slightly before folding. The dampness permits the paper to
                        be
                        folded
                        into soft curves, which then harden in durability as the paper dries.</p>
                </li>
                <li>
                    <p>Crumpled: A technique created by Paul Jackson and developed by Vincent Floderer that involves
                        the
                        crumpling of the paper before folding. This technique can produce highly realistic organic
                        forms.</p>
                </li>
            </ol>
        </div>
        <div class="divider"></div>
    </article>
